public class Mid {


    private double b_mid;
    private double delta_mid;

    public Mid(double b_mid, double delta_mid) {

        this.b_mid = b_mid;
        this.delta_mid = delta_mid;
    }



    public double getB_mid() {
        return b_mid;
    }

    public void setB_mid(double b_mid) {
        this.b_mid = b_mid;
    }

    public double getDelta_mid() {
        return delta_mid;
    }

    public void setDelta_mid(double delta_mid) {
        this.delta_mid = delta_mid;
    }
}
